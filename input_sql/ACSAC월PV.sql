--     5) TEMPACRM_PVПЁ ПљАГРЮШИПјБтКЛ СЄКИ UPDATE

      UPDATE  TEMPACRM_PV
         SET  MЦїРЮЦЎРћИГАЁДЩРЏШПФЋЕхМвСіПЉКЮ     = T2.MЦїРЮЦЎРћИГАЁДЩРЏШПФЋЕхМвСіПЉКЮ
            , ИЖРЯИЎСіРћИГДыЛѓРЏШПФЋЕхМвСіПЉКЮ    = T2.ИЖРЯИЎСіРћИГДыЛѓРЏШПФЋЕхМвСіПЉКЮ

        FROM  TEMPACRM_PV        T1 ,
              DM.ПљАГРЮШИПјСЄКИ  T2
       WHERE  T2.БтСиГтПљ  = EDW_РќПљ                                                               
         AND  T1.АэАДЙјШЃ  = T2.АэАДЙјШЃ  ;                                                     
         
      COMMIT;
         

