      UPDATE  TEMPACRM_PV
         SET  ПЌУМШИТї_ЧіРчШИТї            = T2.ПЌУМШИТї_ЧіРчШИТї

        FROM  TEMPACRM_PV    T1,
              DM.ПљУЄБЧРмОз  T2
       WHERE  T2.БтСиГтПљ  = EDW_РќПљ
         AND  T2.ФЋЕхАГРЮЙ§РЮШИПјБИКаФкЕх='1'
         AND  T1.АэАДЙјШЃ  = T2.АэАДЙјШЃ  ;

      COMMIT;

