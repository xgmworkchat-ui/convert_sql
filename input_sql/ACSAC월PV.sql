--  1) DM.ПљАГРЮКЛРЮШИПјНЧРћ PV И№С§Дм УпУтЧЯПЉ INSERT

     INSERT  INTO TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћ
             (
                АэАДЙјШЃ                        ,    -- DM.ПљАГРЮКЛРЮШИПјНЧРћ	
                СжЙЮЙјШЃ                        ,                               
                РЏШПШИПјПЉКЮ                    ,                               
                СЄЛѓШИПјПЉКЮ                    ,                               
                ИЎНКХЉЕюБо                      ,                                          
                РЬПыКёСп_РЯНУКв            	,                               
                РЬПыКёСп_ЧвКЮ              	,                               
                ЧвКЮМіМіЗсЕюБо                  ,    -- ЧвКЮРЬРк ЛъУтНУ ЛчПы    
                ЧвКЮМіМіЗсРВ                    ,                               
                CAМіМіЗсЕюБо                    ,    -- CAРЬРк ЛъУтНУ ЛчПы      
                CAМіМіЗсРВ                                                      
             )
      SELECT
              T1.АэАДЙјШЃ                                                                               AS АэАДЙјШЃ
            , T1.СжЙЮЙјШЃ                                                                               AS СжЙЮЙјШЃ
            , T1.РЏШПШИПјПЉКЮ                                                                           AS РЏШПШИПјПЉКЮ
            , T1.СЄЛѓШИПјПЉКЮ                                                                           AS СЄЛѓШИПјПЉКЮ
            , CASE WHEN T1.ИЎНКХЉЕюБо='' THEN '00' ELSE T1.ИЎНКХЉЕюБо END                               AS ИЎНКХЉЕюБо
            , CASE WHEN (T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв >0  and   T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ > 0)
                   	THEN  T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв / (T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв + T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ)
                   WHEN (T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв <=0 and  T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв+ T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ > 0) THEN 0
                   WHEN (T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ <=0 and   T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв+ T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ > 0) THEN 1
                   ELSE  T2.РЬПыКёСп_РЯНУКв_ХИЛч
               END                                                                                      AS РЬПыКёСп_РЯНУКв
            , CASE WHEN (T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв >0  and   T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ > 0)
                   	THEN  T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ / (T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв + T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ)
                   WHEN (T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв <=0 and  T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв+ T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ > 0) THEN 1
                   WHEN (T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ <=0 and   T1.РЬПыБнОз_АГРЮКЛРЮ_РЯНУКв+ T1.РЬПыБнОз_АГРЮКЛРЮ_ЧвКЮ > 0) THEN 0
                   ELSE  T2.РЬПыКёСп_ЧвКЮ_ХИЛч
               END                                                                                      AS РЬПыКёСп_ЧвКЮ               
            , T1.ЧвКЮМіМіЗсЕюБо                                                                         AS ЧвКЮМіМіЗсЕюБо
            , 0                                                                                         AS ЧвКЮМіМіЗсРВ
            , T1.CAМіМіЗсЕюБо                                                                           AS CAМіМіЗсЕюБо
            , 0                                                                                         AS CAМіМіЗсРВ

       FROM  	DM.ПљАГРЮКЛРЮШИПјНЧРћ  T1, ACRM.CRMДмАЁ T2
       WHERE  	T1.БтСиГтПљ   = EDW_РќПљ
       AND	T1.РЏШПШИПјПЉКЮ=1
       AND	T1.КЛРЮАЁСЗБИКаФкЕх_ЙпБоБтСи in ('1','3')	
       AND	T1.БтСиГтПљ=T2.БтСиГтПљ  ;

      COMMIT;

