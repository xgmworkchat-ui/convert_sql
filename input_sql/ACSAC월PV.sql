--     5-2) TEMPACRM_PVПЁ УЛБИПЉКЮ  UPDATE

      UPDATE  TEMPACRM_PV
         SET  УЛБИПЉКЮ     = CASE WHEN T2.УЛБИПЉКЮ_АГРЮ>0  THEN 1 ELSE 0 END
        FROM  TEMPACRM_PV        			T1 ,
             (SELECT АэАДЙјШЃ,
             		sum(УЛБИПЉКЮ) 	AS УЛБИПЉКЮ_АГРЮ
              FROM  	DM.ПљФЋЕхУЛБИРдБн
              WHERE  	ФЋЕхАГРЮЙ§РЮШИПјБИКаФкЕх ='1'
              AND	БтСиГтПљ= EDW_РќПљ           
              GROUP BY АэАДЙјШЃ   )               	T2
       WHERE T1.АэАДЙјШЃ  = T2.АэАДЙјШЃ  ;     
         
      COMMIT;
         
         
-- call sp_dmjob('PV');  -- 5-2


--     6) TEMPACRM_PVПЁ ЧвКЮ/CA РЬРк СЄКИ UPDATE (ЧтШФ ХзРЬКэ УпАЁШФ АЊРћРч)

      UPDATE  TEMPACRM_PV
         SET  РЬПыМіМіЗс_ЧвКЮ    = (T1.РЬПыБнОз_ЧвКЮ_ХИЛч * (cast((1+T2.НХПыАјПЉБтАЃ_ЧвКЮ) as numeric(9,6))  / 24) ) * T1.ЧвКЮМіМіЗсРВ/100
            , РЬПыМіМіЗс_CA      = (T1.РЬПыБнОз_CA_ХИЛч   * (cast(T2.НХПыАјПЉБтАЃ_CA as numeric(9,6)) / 365)) * T1.CAМіМіЗсРВ/100

        FROM  TEMPACRM_PV T1, ACRM.CRMДмАЁ T2
       WHERE  T2.БтСиГтПљ = EDW_РќПљ ;

      COMMIT;


