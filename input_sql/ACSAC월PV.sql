          , T1.РЬПыБнОз_РЯНУКв_ХИЛч * T2.АЁИЭСЁМіМіЗсРВ_РЯНУКв                            AS АЁИЭСЁМіМіЗс_РЯНУКв
          , T1.РЬПыБнОз_ЧвКЮ_ХИЛч   * T2.АЁИЭСЁМіМіЗсРВ_ЧвКЮ                              AS АЁИЭСЁМіМіЗс_ЧвКЮ
          , T1.РЬПыМіМіЗс_ЧвКЮ                                                            AS РЬПыМіМіЗс_ЧвКЮ                    -- ЧтШФ УпАЁ (СжМЎКЮКа PROGRAM LOGIC ТќСЖ)
          , T1.РЬПыМіМіЗс_CA                                                              AS РЬПыМіМіЗс_CA                      -- ЧтШФ УпАЁ (СжМЎКЮКа PROGRAM LOGIC ТќСЖ)
          , T1.РЬПыБнОз_CA_ХИЛч     * T2.УыБоМіМіЗсРВ_CA                                  AS УыБоМіМіЗс_CA
          , ((T1.РЬПыБнОз_РЯНУКв_ХИЛч *  (cast(T2.НХПыАјПЉБтАЃ_РЯНУКв as numeric(9,6)) / 365))  +
             (T1.РЬПыБнОз_ЧвКЮ_ХИЛч   * (cast((1+T2.НХПыАјПЉБтАЃ_ЧвКЮ) as numeric(9,6))  / 24))  +
             (T1.РЬПыБнОз_CA_ХИЛч     *    (cast(T2.НХПыАјПЉБтАЃ_CA as numeric(9,6))     / 365))) * T2.ТїРдРЬРк       AS БнРЖКёПы

          , (T1.РЬПыБнОз_РЯНУКв_ХИЛч / T2.ЦђБеАЧКАРЬПыБнОз_АГРЮ_РЯНУКв    +
             T1.РЬПыБнОз_ЧвКЮ_ХИЛч   / T2.ЦђБеАЧКАРЬПыБнОз_АГРЮ_ЧвКЮ)     * T2.АХЗЁНТРЮКёПы   AS НТРЮКёПы

          , (T1.РЬПыБнОз_РЯНУКв_ХИЛч / T2.ЦђБеАЧКАРЬПыБнОз_АГРЮ_РЯНУКв    +
             T1.РЬПыБнОз_ЧвКЮ_ХИЛч   / T2.ЦђБеАЧКАРЬПыБнОз_АГРЮ_ЧвКЮ)     * T2.РќЧЅИХРдКёПы   AS ИХРдКёПы

          , CASE WHEN T1.УЛБИПЉКЮ=0  AND T1.РЬПыБнОз_РЯНУКв_ХИЛч + T1.РЬПыБнОз_ЧвКЮ_ХИЛч + T1.РЬПыБнОз_CA_ХИЛч > 0
                 THEN T2.УЛБИКёПы
                 ELSE 0
            END                                                                               AS УЛБИКёПы
          , CASE WHEN T1.УЛБИПЉКЮ = 0 AND T1.РЬПыБнОз_РЯНУКв_ХИЛч + T1.РЬПыБнОз_ЧвКЮ_ХИЛч + T1.РЬПыБнОз_CA_ХИЛч > 0
                 THEN T2.РдБнКёПы
                 ELSE 0
            END                                                                               AS РдБнКёПы
          , CASE WHEN T1.MЦїРЮЦЎРћИГАЁДЩРЏШПФЋЕхМвСіПЉКЮ = 1
                 THEN (T1.РЬПыБнОз_РЯНУКв_ХИЛч * T2.MЦїРЮЦЎКёРВ_РЯНУКв ) +
                      (T1.РЬПыБнОз_ЧвКЮ_ХИЛч   * T2.MЦїРЮЦЎКёРВ_ЧвКЮ   )
                 ELSE 0
            END                                                                               AS MЦЧУЫКё
          , CASE WHEN T1.ИЖРЯИЎСіРћИГДыЛѓРЏШПФЋЕхМвСіПЉКЮ = 1
                 THEN (T1.РЬПыБнОз_РЯНУКв_ХИЛч * T2.ИЖРЯИЎСіКёРВ_РЯНУКв )+
                      (T1.РЬПыБнОз_ЧвКЮ_ХИЛч   * T2.ИЖРЯИЎСіКёРВ_ЧвКЮ   )
                 ELSE 0
            END                                                                               AS ИЖРЯИЎСіКёПы
          , (T1.РЬПыБнОз_РЯНУКв_ХИЛч + T1.РЬПыБнОз_ЧвКЮ_ХИЛч) * T2.ЧвРЮМ­КёНККёРВ             AS ЧвРЮМ­КёНК_МіМіЗс
          , (T1.РЬПыБнОз_РЯНУКв_ХИЛч + T1.РЬПыБнОз_ЧвКЮ_ХИЛч) * T2.ЦЧУЫМ­КёНККёРВ             AS ЦЧУЫМ­КёНК_МіМіЗс
          , (T1.РЬПыБнОз_РЯНУКв_ХИЛч * T2.КъЗЃЕхЗЮОтЦМКёРВ_РЯНУКв +
             T1.РЬПыБнОз_ЧвКЮ_ХИЛч   * T2.КъЗЃЕхЗЮОтЦМКёРВ_ЧвКЮ   +
             T1.РЬПыБнОз_CA_ХИЛч     * T2.КъЗЃЕхЗЮОтЦМКёРВ_CA)                                AS КъЗЃЕхЗЮП­ЦМ
          , NOW(*)                                                                            AS DMРћРчРЯНУ

      FROM  TEMPACRM_PV                   T1,
            ACRM.CRMДмАЁ                  T2

     WHERE  T2.БтСиГтПљ  = DATEFORMAT(MONTHS(DATE(''$1''||''01''),-1) ,''YYYYMM'')       ';
      CALL  SP_EXTRACT(EXTRACT_DAT, EXTRACT_SQL);

