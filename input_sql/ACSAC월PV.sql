--     3) DM.ПљАГРЮКЛРЮШИПјНЧРћ, DM.ПљКЙМіФЋЕхРЬПыНЧРћ PV И№С§Дм ХыЧеЧЯПЉ INSERT

     INSERT  INTO TEMPACRM_PV
             (
                АэАДЙјШЃ                                ,
                СжЙЮЙјШЃ                                ,
                РЏШПШИПјПЉКЮ                            ,
                СЄЛѓШИПјПЉКЮ                            ,
                ИЎНКХЉЕюБо                              ,
                ПЌУМШИТї_ЧіРчШИТї                	,
                РЬПыБнОз_РЯНУКв_ХИЛч                    ,  -- ХИЛч РЬПыБнОз РЯНУКв ; ДчЛч РЯНУКвКёСп РћПы
                РЬПыБнОз_ЧвКЮ_ХИЛч                      ,  -- ХИЛч РЬПыБнОз ЧвКЮ   ; ДчЛч ЧвКЮКёСп РћПы
                РЬПыБнОз_CA_ХИЛч                        ,  -- ХИЛч РЬПыБнОз CA
                УЛБИПЉКЮ				,
                MЦїРЮЦЎРћИГАЁДЩРЏШПФЋЕхМвСіПЉКЮ         ,
                ИЖРЯИЎСіРћИГДыЛѓРЏШПФЋЕхМвСіПЉКЮ        ,
                ЧвКЮМіМіЗсРВ                            ,  -- РЬРкРВЛъУт ; ЧтШФ ХзРЬКэУпАЁ (ОЦЗЁСжМЎ ТќСЖ)
                CAМіМіЗсРВ                              ,  -- РЬРкРВЛъУт ; ЧтШФ ХзРЬКэУпАЁ (ОЦЗЁСжМЎ ТќСЖ)
                РЬПыМіМіЗс_ЧвКЮ                         ,  -- РЬРкРВЛъУт ; ЧтШФ ХзРЬКэУпАЁ (ОЦЗЁСжМЎ ТќСЖ)
                РЬПыМіМіЗс_CA                              -- РЬРкРВЛъУт ; ЧтШФ ХзРЬКэУпАЁ (ОЦЗЁСжМЎ ТќСЖ)
             )
      SELECT
              T1.АэАДЙјШЃ                                                               AS АэАДЙјШЃ
            , T1.СжЙЮЙјШЃ                                                               AS СжЙЮЙјШЃ
            , T1.РЏШПШИПјПЉКЮ                                                           AS РЏШПШИПјПЉКЮ
            , T1.СЄЛѓШИПјПЉКЮ                                                           AS СЄЛѓШИПјПЉКЮ
            , T1.ИЎНКХЉЕюБо                                                             AS ИЎНКХЉЕюБо
            , 0                                                                         AS ПЌУМШИТї_ЧіРчШИТї
            , T1.РЬПыКёСп_РЯНУКв * T2.РЬПыБнОз_НХЦЧ_ХИЛч                                AS РЬПыБнОз_РЯНУКв_ХИЛч
            , T1.РЬПыКёСп_ЧвКЮ * T2.РЬПыБнОз_НХЦЧ_ХИЛч                              	AS РЬПыБнОз_ЧвКЮ_ХИЛч
            , T2.РЬПыБнОз_CA_ХИЛч                                                       AS РЬПыБнОз_CA_ХИЛч
            , 0										AS УЛБИПЉКЮ
            , 0                                                                         AS MЦїРЮЦЎРћИГАЁДЩРЏШПФЋЕхМвСіПЉКЮ
            , 0                                                                         AS ИЖРЯИЎСіРћИГДыЛѓРЏШПФЋЕхМвСіПЉКЮ
            , T1.ЧвКЮМіМіЗсРВ                                                           AS ЧвКЮМіМіЗсРВ
            , T1.CAМіМіЗсРВ                                                             AS CAМіМіЗсРВ
            , 0                                                                         AS РЬПыМіМіЗс_ЧвКЮ
            , 0                                                                         AS РЬПыМіМіЗс_CA

        FROM  TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћ  T1, TEMPACRM_ПљКЙМіФЋЕхРЬПыНЧРћ T2
       WHERE  T1.СжЙЮЙјШЃ = T2.СжЙЮЙјШЃ ;

      COMMIT;

