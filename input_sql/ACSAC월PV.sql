     -- УжБй РћПыЕШ ЧвКЮМіМіЗсРВ УпУт
     INSERT  INTO TEMPACRM_ЧвКЮМіМіЗсРВ
             (
                ЧвКЮМіМіЗсЕюБо          ,
                ЧвКЮМіМіЗсРВ
             )
      SELECT
              T1.ЧвКЮМіМіЗсЕюБо                                 AS ЧвКЮМіМіЗсЕюБо
            , T1.ЧвКЮМіМіЗсРВ                                   AS ЧвКЮМіМіЗсРВ

        FROM  DW.ЧвКЮМіМіЗсРВ  T1, 	ACRM.CRMДмАЁ T2
       WHERE  T1.ФЋЕхАГРЮЙ§РЮШИПјБИКаФкЕх = '1' --АГРЮ
         AND  T2.БтСиГтПљ=EDW_РќПљ
         AND  T1.РћПыРЯРк = ( SELECT MAX(РћПыРЯРк)
                                FROM DW.ЧвКЮМіМіЗсРВ
                               WHERE ФЋЕхАГРЮЙ§РЮШИПјБИКаФкЕх = '1' ) 
	AND T1.НУРлЧвКЮАГПљМі<= T2.НХПыАјПЉБтАЃ_ЧвКЮ
	AND T1.СОЗсЧвКЮАГПљМі>= T2.НХПыАјПЉБтАЃ_ЧвКЮ  ;
      COMMIT;

     -- УжБй РћПыЕШ CAМіМіЗсРВ РћПы
     INSERT  INTO TEMPACRM_CAМіМіЗсРВ
             (
                CAМіМіЗсЕюБо            ,
                CAМіМіЗсРВ
             )
      SELECT
              T1.ЧіБнМ­КёНКМіМіЗсЕюБо                           AS CAМіМіЗсЕюБо
            , MIN(T1.ЧіБнМ­КёНКМіМіЗсРВ)                        AS CAМіМіЗсРВ

        FROM  DW.ЧіБнМ­КёНКМіМіЗсРВ  T1,  ACRM.CRMДмАЁ T2
       WHERE  T1.ФЋЕхАГРЮЙ§РЮШИПјБИКаФкЕх = '1' --АГРЮ
         AND  T2.БтСиГтПљ=EDW_РќПљ
         AND  T1.РћПыРЯРк = ( SELECT MAX(РћПыРЯРк)
                                FROM DW.ЧіБнМ­КёНКМіМіЗсРВ
                               WHERE ФЋЕхАГРЮЙ§РЮШИПјБИКаФкЕх = '1' )
      AND T1.НУРлНХПыАјПЉРЯМі<= T2.НХПыАјПЉБтАЃ_CA
      AND T1.СОЗсНХПыАјПЉРЯМі>= T2.НХПыАјПЉБтАЃ_CA
      GROUP BY   T1.ЧіБнМ­КёНКМіМіЗсЕюБо  ;
    
      COMMIT;


     -- TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћПЁ ЧвКЮМіМіЗсРВ UPDATE
      UPDATE  TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћ
         SET  ЧвКЮМіМіЗсРВ     = T2.ЧвКЮМіМіЗсРВ

        FROM  TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћ                T1,
              TEMPACRM_ЧвКЮМіМіЗсРВ                      T2
       WHERE  T1.ЧвКЮМіМіЗсЕюБо  = T2.ЧвКЮМіМіЗсЕюБо  ;

      COMMIT;

     -- TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћПЁ CAМіМіЗсРВ UPDATE
      UPDATE  TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћ
         SET  CAМіМіЗсРВ     = T2.CAМіМіЗсРВ

        FROM  TEMPACRM_ПљАГРЮКЛРЮШИПјНЧРћ                T1,
              TEMPACRM_CAМіМіЗсРВ                        T2
       WHERE  T1.CAМіМіЗсЕюБо  = T2.CAМіМіЗсЕюБо  ;

      COMMIT;
