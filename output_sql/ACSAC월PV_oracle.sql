--  1) DM.월개인본인회원실적 PV 모집단 추출하여 INSERT

     INSERT  INTO TEMPACRM_월개인본인회원실적
             (
                고객번호                        ,    -- DM.월개인본인회원실적	
                주민번호                        ,                               
                유효회원여부                    ,                               
                정상회원여부                    ,                               
                리스크등급                      ,                                          
                이용비중_일시불            	,                               
                이용비중_할부              	,                               
                할부수수료등급                  ,    -- 할부이자 산출시 사용    
                할부수수료율                    ,                               
                CA수수료등급                    ,    -- CA이자 산출시 사용      
                CA수수료율                                                      
             )
      SELECT
              T1.고객번호                                                                               AS 고객번호
            , T1.주민번호                                                                               AS 주민번호
            , T1.유효회원여부                                                                           AS 유효회원여부
            , T1.정상회원여부                                                                           AS 정상회원여부
            , CASE WHEN T1.리스크등급='' THEN '00' ELSE T1.리스크등급 END                               AS 리스크등급
            , CASE WHEN (T1.이용금액_개인본인_일시불 >0  and   T1.이용금액_개인본인_할부 > 0)
                   	THEN  T1.이용금액_개인본인_일시불 / (T1.이용금액_개인본인_일시불 + T1.이용금액_개인본인_할부)
                   WHEN (T1.이용금액_개인본인_일시불 <=0 and  T1.이용금액_개인본인_일시불+ T1.이용금액_개인본인_할부 > 0) THEN 0
                   WHEN (T1.이용금액_개인본인_할부 <=0 and   T1.이용금액_개인본인_일시불+ T1.이용금액_개인본인_할부 > 0) THEN 1
                   ELSE  T2.이용비중_일시불_타사
               END                                                                                      AS 이용비중_일시불
            , CASE WHEN (T1.이용금액_개인본인_일시불 >0  and   T1.이용금액_개인본인_할부 > 0)
                   	THEN  T1.이용금액_개인본인_할부 / (T1.이용금액_개인본인_일시불 + T1.이용금액_개인본인_할부)
                   WHEN (T1.이용금액_개인본인_일시불 <=0 and  T1.이용금액_개인본인_일시불+ T1.이용금액_개인본인_할부 > 0) THEN 1
                   WHEN (T1.이용금액_개인본인_할부 <=0 and   T1.이용금액_개인본인_일시불+ T1.이용금액_개인본인_할부 > 0) THEN 0
                   ELSE  T2.이용비중_할부_타사
               END                                                                                      AS 이용비중_할부               
            , T1.할부수수료등급                                                                         AS 할부수수료등급
            , 0                                                                                         AS 할부수수료율
            , T1.CA수수료등급                                                                           AS CA수수료등급
            , 0                                                                                         AS CA수수료율

       FROM  	DM.월개인본인회원실적  T1, ACRM.CRM단가 T2
       WHERE  	T1.기준년월   = EDW_전월
       AND	T1.유효회원여부=1
       AND	T1.본인가족구분코드_발급기준 in ('1','3')	
       AND	T1.기준년월=T2.기준년월  ;

      COMMIT;

