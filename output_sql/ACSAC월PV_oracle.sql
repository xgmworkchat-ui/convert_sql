--     6) TEMPACRM_PV에 할부/CA 이자 정보 UPDATE (향후 테이블 추가후 값적재)

      UPDATE  TEMPACRM_PV
    이용수수료_할부 := (T1.이용금액_할부_타사 * (cast((1+T2.신용공여기간_할부) as NUMBER(9,6))  / 24) ) * T1.할부수수료율/100
            , 이용수수료_CA      = (T1.이용금액_CA_타사   * (cast(T2.신용공여기간_CA as NUMBER(9,6)) / 365)) * T1.CA수수료율/100

        FROM  TEMPACRM_PV T1, ACRM.CRM단가 T2
       WHERE  T2.기준년월 = EDW_전월 ;

      COMMIT;


