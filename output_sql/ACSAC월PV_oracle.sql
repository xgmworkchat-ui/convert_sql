--     5-2) TEMPACRM_PV에 청구여부  UPDATE

      UPDATE  TEMPACRM_PV
    청구여부 := CASE WHEN T2.청구여부_개인>0  THEN 1 ELSE 0 END
        FROM  TEMPACRM_PV        			T1 ,
             (SELECT 고객번호,
             		sum(청구여부) 	AS 청구여부_개인
              FROM  	DM.월카드청구입금
              WHERE  	카드개인법인회원구분코드 ='1'
              AND	기준년월= EDW_전월           
              GROUP BY 고객번호   )               	T2
       WHERE T1.고객번호  = T2.고객번호  ;     
         
      COMMIT;
         
         
