     -- 최근 적용된 할부수수료율 추출
     INSERT  INTO TEMPACRM_할부수수료율
             (
                할부수수료등급          ,
                할부수수료율
             )
      SELECT
              T1.할부수수료등급                                 AS 할부수수료등급
            , T1.할부수수료율                                   AS 할부수수료율

        FROM  DW.할부수수료율  T1, 	ACRM.CRM단가 T2
       WHERE  T1.카드개인법인회원구분코드 = '1' --개인
         AND  T2.기준년월=EDW_전월
         AND  T1.적용일자 = ( SELECT MAX(적용일자)
                                FROM DW.할부수수료율
                               WHERE 카드개인법인회원구분코드 = '1' ) 
	AND T1.시작할부개월수<= T2.신용공여기간_할부
	AND T1.종료할부개월수>= T2.신용공여기간_할부  ;
      COMMIT;

     -- 최근 적용된 CA수수료율 적용
     INSERT  INTO TEMPACRM_CA수수료율
             (
                CA수수료등급            ,
                CA수수료율
             )
      SELECT
              T1.현금서비스수수료등급                           AS CA수수료등급
            , MIN(T1.현금서비스수수료율)                        AS CA수수료율

        FROM  DW.현금서비스수수료율  T1,  ACRM.CRM단가 T2
       WHERE  T1.카드개인법인회원구분코드 = '1' --개인
         AND  T2.기준년월=EDW_전월
         AND  T1.적용일자 = ( SELECT MAX(적용일자)
                                FROM DW.현금서비스수수료율
                               WHERE 카드개인법인회원구분코드 = '1' )
      AND T1.시작신용공여일수<= T2.신용공여기간_CA
      AND T1.종료신용공여일수>= T2.신용공여기간_CA
      GROUP BY   T1.현금서비스수수료등급  ;
    
      COMMIT;


     -- TEMPACRM_월개인본인회원실적에 할부수수료율 UPDATE
      UPDATE  TEMPACRM_월개인본인회원실적
    할부수수료율 := T2.할부수수료율

        FROM  TEMPACRM_월개인본인회원실적                T1,
              TEMPACRM_할부수수료율                      T2
       WHERE  T1.할부수수료등급  = T2.할부수수료등급  ;

      COMMIT;

     -- TEMPACRM_월개인본인회원실적에 CA수수료율 UPDATE
      UPDATE  TEMPACRM_월개인본인회원실적
    CA수수료율 := T2.CA수수료율

        FROM  TEMPACRM_월개인본인회원실적                T1,
              TEMPACRM_CA수수료율                        T2
       WHERE  T1.CA수수료등급  = T2.CA수수료등급  ;

      COMMIT;
