          , (T1.이용금액_일시불_타사 / T2.평균건별이용금액_개인_일시불    +
             T1.이용금액_할부_타사   / T2.평균건별이용금액_개인_할부)     * T2.거래승인비용   AS 승인비용

          , (T1.이용금액_일시불_타사 / T2.평균건별이용금액_개인_일시불    +
             T1.이용금액_할부_타사   / T2.평균건별이용금액_개인_할부)     * T2.전표매입비용   AS 매입비용

          , CASE WHEN T1.청구여부=0  AND T1.이용금액_일시불_타사 + T1.이용금액_할부_타사 + T1.이용금액_CA_타사 > 0
                 THEN T2.청구비용
                 ELSE 0
            END                                                                               AS 청구비용
          , CASE WHEN T1.청구여부 = 0 AND T1.이용금액_일시불_타사 + T1.이용금액_할부_타사 + T1.이용금액_CA_타사 > 0
                 THEN T2.입금비용
                 ELSE 0
            END                                                                               AS 입금비용
          , CASE WHEN T1.M포인트적립가능유효카드소지여부 = 1
                 THEN (T1.이용금액_일시불_타사 * T2.M포인트비율_일시불 ) +
                      (T1.이용금액_할부_타사   * T2.M포인트비율_할부   )
                 ELSE 0
            END                                                                               AS M판촉비
          , CASE WHEN T1.마일리지적립대상유효카드소지여부 = 1
                 THEN (T1.이용금액_일시불_타사 * T2.마일리지비율_일시불 )+
                      (T1.이용금액_할부_타사   * T2.마일리지비율_할부   )
                 ELSE 0
            END                                                                               AS 마일리지비용
          , (T1.이용금액_일시불_타사 + T1.이용금액_할부_타사) * T2.할인서비스비율             AS 할인서비스_수수료
          , (T1.이용금액_일시불_타사 + T1.이용금액_할부_타사) * T2.판촉서비스비율             AS 판촉서비스_수수료
          , (T1.이용금액_일시불_타사 * T2.브랜드로얄티비율_일시불 +
             T1.이용금액_할부_타사   * T2.브랜드로얄티비율_할부   +
             T1.이용금액_CA_타사     * T2.브랜드로얄티비율_CA)                                AS 브랜드로열티
          , SYSDATE                                                                            AS DM적재일시

      FROM  TEMPACRM_PV                   T1,
            ACRM.CRM단가                  T2

     WHERE  T2.기준년월  = DATEFORMAT(ADD_MONTHS(DATE(''$1''||''01''), -1) ,''YYYYMM'')       ';
