            , 마일리지적립대상유효카드소지여부    = T2.마일리지적립대상유효카드소지여부

        FROM  TEMPACRM_PV        T1 ,
              DM.월개인회원정보  T2
       WHERE  T2.기준년월  = EDW_전월                                                               
