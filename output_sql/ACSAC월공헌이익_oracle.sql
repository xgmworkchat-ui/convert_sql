                이용건수_개인_할부              ,
                이용건수_개인_CA		,
                이용건수_개인_정상론		,
                이용회원수_일시불       	,
                이용회원수_할부         	,
                이용회원수_CA           	,
                이용회원수_정상론       	,
                이용회원수_신판         	,
                이용회원수_기본
             )
      SELECT
              SUM(이용금액_개인본인_일시불    )                          AS 이용금액_개인_일시불
            , SUM(이용금액_개인본인_할부      )                          AS 이용금액_개인_할부
            , SUM(이용금액_개인본인_CA        )                          AS 이용금액_개인_CA
            , SUM(이용금액_개인본인_정상론    )                          AS 이용금액_개인_정상론
            , SUM(가맹점수수료_개인본인_일시불)                          AS 가맹점수수료_개인_일시불
            , SUM(가맹점수수료_개인본인_할부  )                          AS 가맹점수수료_개인_할부
            , SUM(수수료_개인본인_CA_취급     )                          AS 수수료_개인_CA_취급
            , SUM(수수료_개인본인_CA_거래     )                          AS 수수료_개인_CA_거래
            , SUM(수수료_개인본인_정상론_취급 )                          AS 수수료_개인_정상론_취급
            , SUM(수수료_개인본인_정상론_거래 )                          AS 수수료_개인_정상론_거래
            , SUM(수수료_개인본인_할부        )                          AS 수수료_개인_할부
            , SUM(수수료_개인본인_CA_이용     )                          AS 수수료_개인_CA_이용
            , SUM(수수료_개인본인_정상론_이용 )                          AS 수수료_개인_정상론_이용
            , SUM(이용건수_개인본인_일시불    )                          AS 이용건수_개인_일시불
            , SUM(이용건수_개인본인_할부      )                          AS 이용건수_개인_할부
            , SUM(이용건수_개인본인_CA        )                          AS 이용건수_개인_CA
            , SUM(이용건수_개인본인_정상론    )                          AS 이용건수_개인_정상론
            , SUM(CASE WHEN 이용여부_일시불  = 1 THEN 1 ELSE 0 END)  	 AS 이용회원수_일시불
            , SUM(CASE WHEN 이용여부_할부    = 1 THEN 1 ELSE 0 END)  	 AS 이용회원수_할부
            , SUM(CASE WHEN 이용여부_CA      = 1 THEN 1 ELSE 0 END)  	 AS 이용회원수_CA
            , SUM(CASE WHEN 이용여부_정상론  = 1 THEN 1 ELSE 0 END)  	 AS 이용회원수_정상론
            , SUM(CASE WHEN 이용여부_신판    = 1 THEN 1 ELSE 0 END)  	 AS 이용회원수_신판
            , SUM(CASE WHEN 이용여부_기본    = 1 THEN 1 ELSE 0 END)  	 AS 이용회원수_기본

        FROM  DM.월개인본인회원실적  
       WHERE  기준년월   = '$1' 
       AND    본인가족구분코드_발급기준 <>'4'
       AND    리스크등급<>'';

      COMMIT;


