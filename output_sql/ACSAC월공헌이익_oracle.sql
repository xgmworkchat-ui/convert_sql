--     5) 대손율합계 적재

     
     INSERT  INTO TEMPACRM_대손비용
             (
              대손충당비용_일시불           ,
              대손충당비용_할부             ,
              대손충당비용_CA               ,
              대손충당비용_정상론
             )
      SELECT
              SUM(이용금액_개인본인_일시불 * RP대손율_일시불)                                     AS 대손충당비용_일시불
            , SUM(이용금액_개인본인_할부   * RP대손율_할부  )                                     AS 대손충당비용_할부
            , SUM(이용금액_개인본인_CA     * RP대손율_CA    )                                     AS 대손충당비용_CA
            , SUM(이용금액_개인본인_정상론 * RP대손율_정상론)                                     AS 대손충당비용_정상론

        FROM  TEMPACRM_RP대손율   ;

      COMMIT;

