--     4) TEMPACRM_RP대손율에 대손율 UPDATE(1)

      -- 일시불
      UPDATE  TEMPACRM_RP대손율
    RP대손율_일시불 := T2.대손율_일시불

       FROM   TEMPACRM_RP대손율    T1 ,
              ACRM.CRMRP대손율     T2
      WHERE   T2.기준년월                     = '$1'
        AND   T1.리스크등급                   = T2.리스크등급 ;

      COMMIT;


      -- 할부
      UPDATE  TEMPACRM_RP대손율
    RP대손율_할부 := T2.대손율_할부

       FROM   TEMPACRM_RP대손율    T1 ,
              ACRM.CRMRP대손율         T2
      WHERE   T2.기준년월                     = '$1'
        AND   T1.리스크등급                     = T2.리스크등급 ;

      COMMIT;


      -- CA
      UPDATE  TEMPACRM_RP대손율
    RP대손율_CA := T2.대손율_CA

       FROM   TEMPACRM_RP대손율    T1 ,
              ACRM.CRMRP대손율         T2
      WHERE   T2.기준년월                     = '$1'
        AND   T1.리스크등급                     = T2.리스크등급 ;

      COMMIT;


      -- 정상론
      UPDATE  TEMPACRM_RP대손율
    RP대손율_정상론 := T2.대손율_카드론

       FROM   TEMPACRM_RP대손율    T1 ,
              ACRM.CRMRP대손율         T2
      WHERE   T2.기준년월                     = '$1'
        AND   T1.리스크등급                     = T2.리스크등급 ;

      COMMIT;


