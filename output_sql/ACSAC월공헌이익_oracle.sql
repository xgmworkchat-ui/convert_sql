--     10) ACRM.공헌이익 테이블에 공헌이익 UPDATE

      UPDATE  ACRM.공헌이익
    공헌이익_일시불 := T1.가맹점수수료_일시불
                                           -  T1.금융비용_일시불        
                                           -  T1.PROCESS비용_일시불     
                                           -  T1.상품서비스비용_일시불  
                                           -  T1.대손충당비용_일시불        
            , 공헌이익_할부		=    (T1.가맹점수수료_할부 + T1.이용수수료_할부)
                                           -  T1.금융비용_할부                     
                                           -  T1.PROCESS비용_할부                  
                                           -  T1.상품서비스비용_할부               
                                           -  T1.대손충당비용_할부              
            , 공헌이익_CA       	=    (T1.취급수수료_CA         + T1.이용수수료_CA)
                                           -  T1.금융비용_CA             
                                           -  T1.PROCESS비용_CA          
                                           -  T1.브랜드로열티_CA         
                                           -  T1.대손충당비용_CA     
            , 공헌이익_정상론           =    (T1.취급수수료_정상론     + T1.이용수수료_정상론)
                                           -  T1.금융비용_정상론                 
                                           -  T1.PROCESS비용_정상론              
                                           -  T1.자금이체수수료_정상론           
                                           -  T1.대손충당비용_정상론
            , 공헌이익_신판             =    (T1.가맹점수수료_일시불 + T1.가맹점수수료_할부 + T1.이용수수료_할부)
                                           -  T1.금융비용_일시불    -  T1.금융비용_할부    
                                           -  T1.PROCESS비용_신판   -  T1.상품서비스비용_일시불   -  T1.상품서비스비용_할부
                                           -  T1.대손충당비용_일시불   -  T1.대손충당비용_할부 
            , 공헌이익_기본		=    (T1.가맹점수수료_일시불 + T1.가맹점수수료_할부 + T1.이용수수료_할부
            					+ T1.취급수수료_CA         + T1.이용수수료_CA)
                                           -  T1.금융비용_일시불   -  T1.금융비용_할부   -  T1.금융비용_CA      
                                           -  T1.PROCESS비용_기본     
                                           -  T1.상품서비스비용_일시불 -  T1.상품서비스비용_할부 -  T1.브랜드로열티_CA 
                                           -  T1.대손충당비용_일시불    -  T1.대손충당비용_할부 -  T1.대손충당비용_CA       
                         		     
         
        FROM  ACRM.공헌이익      T1 
       WHERE  T1.기준년월 = '$1' ;

      COMMIT;
      
