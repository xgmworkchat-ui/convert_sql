--     3) DM.월개인본인회원실적과 DM.월채권잔액의 연체회차 고객등급 적재. (대손추출)

     
     INSERT  INTO TEMPACRM_RP대손율
             (
                고객번호                                ,
                리스크등급                              ,
                이용금액_개인본인_일시불                ,
                이용금액_개인본인_할부                  ,
                이용금액_개인본인_CA                    ,
                이용금액_개인본인_정상론                ,
                RP대손율_일시불                         ,
                RP대손율_할부                           ,
                RP대손율_CA                             ,
                RP대손율_정상론
             )
      SELECT
              T1.고객번호                                                       AS 고객번호
            , T1.리스크등급                                                     AS 리스크등급
            , T1.이용금액_개인본인_일시불                                       AS 이용금액_개인본인_일시불
            , T1.이용금액_개인본인_할부                                         AS 이용금액_개인본인_할부
            , T1.이용금액_개인본인_CA                                           AS 이용금액_개인본인_CA
            , T1.이용금액_개인본인_정상론                                       AS 이용금액_개인본인_정상론
            , 0                                                                 AS RP대손율_일시불
            , 0                                                                 AS RP대손율_할부
            , 0                                                                 AS RP대손율_CA
            , 0                                                                 AS RP대손율_정상론

        FROM  DM.월개인본인회원실적  T1
       WHERE  T1.기준년월  = '$1'  
       AND    T1.본인가족구분코드_발급기준 <>'4'
       AND    T1.리스크등급<>'';

      COMMIT;

