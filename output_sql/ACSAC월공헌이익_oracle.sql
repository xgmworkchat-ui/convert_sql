--     9) ACRM.공헌이익 테이블에 PROCESS비용/상품서비스비용 UPDATE

      UPDATE  ACRM.공헌이익
    PROCESS비용_일시불 := T1.승인비용_일시불  + T1.매입비용_일시불  + T1.청구비용_일시불 + T1.입금비용_일시불
            , PROCESS비용_할부    = T1.승인비용_할부    + T1.매입비용_할부    + T1.청구비용_할부   + T1.입금비용_할부  
            , PROCESS비용_CA      = T1.청구비용_CA      + T1.입금비용_CA    
            , PROCESS비용_정상론  = T1.청구비용_정상론  + T1.입금비용_정상론
            , PROCESS비용_신판    = T1.승인비용_일시불  + T1.매입비용_일시불 +T1.승인비용_할부 + T1.매입비용_할부 +T1.청구비용_신판 + T1.입금비용_신판  
            , PROCESS비용_기본    = T1.승인비용_일시불  + T1.매입비용_일시불 +T1.승인비용_할부 + T1.매입비용_할부 +T1.청구비용_기본 + T1.입금비용_기본
            , 상품서비스비용_일시불= T1.M포인트비용_일시불 + T1.마일리지비용_일시불 + T1.할인서비스_일시불 + T1.판촉서비스수수료_일시불 + T1.브랜드로열티_일시불
            , 상품서비스비용_할부  = T1.M포인트비용_할부   + T1.마일리지비용_할부   + T1.할인서비스_할부   + T1.판촉서비스수수료_할부   + T1.브랜드로열티_할부 
                                      
        FROM  ACRM.공헌이익      T1,  
              ACRM.CRM단가       T2
       WHERE  T1.기준년월  = T2.기준년월
         AND  T1.기준년월  = '$1'  ;

      COMMIT;

