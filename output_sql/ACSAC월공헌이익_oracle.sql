--     8) ACRM.공헌이익 테이블에 입금비용 UPDATE

      UPDATE  ACRM.공헌이익
    입금비용_일시불 := T1.청구비용_일시불 * T2.입금비용 / T2.청구비용
            , 입금비용_할부             = T1.청구비용_할부   * T2.입금비용 / T2.청구비용
            , 입금비용_CA               = T1.청구비용_CA     * T2.입금비용 / T2.청구비용
            , 입금비용_정상론           = T1.청구비용_정상론 * T2.입금비용 / T2.청구비용
            , 입금비용_신판             = T1.청구비용_신판   * T2.입금비용 / T2.청구비용
            , 입금비용_기본  		= T1.청구비용_기본   * T2.입금비용 / T2.청구비용
                                      
        FROM  ACRM.공헌이익      T1,  
              ACRM.CRM단가       T2
       WHERE  T1.기준년월  = T2.기준년월
         AND  T1.기준년월  = '$1'  ;

      COMMIT;

