--     11) ACRM.공헌이익 테이블에 공헌이익 UPDATE

      UPDATE  ACRM.공헌이익
    공헌이익율_일시불 := T1.공헌이익_일시불 /  T1.이용금액_개인_일시불                                        	
            , 공헌이익율_할부		= T1.공헌이익_할부   /  T1.이용금액_개인_할부                                                 
            , 공헌이익율_CA             = T1.공헌이익_CA     /  T1.이용금액_개인_CA                                                   
            , 공헌이익율_정상론         = T1.공헌이익_정상론 /  T1.이용금액_개인_정상론                                               
            , 공헌이익율_신판		= T1.공헌이익_신판   / (T1.이용금액_개인_일시불 + T1.이용금액_개인_할부)                      
            , 공헌이익율_기본		= T1.공헌이익_기본   / (T1.이용금액_개인_일시불 + T1.이용금액_개인_할부 + T1.이용금액_개인_CA)
         
        FROM  ACRM.공헌이익      T1 
       WHERE  T1.기준년월 = '$1' ;

      COMMIT;


