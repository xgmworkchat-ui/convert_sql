            , T1.이용회원수_정상론     * T4.청구비용 * T4.신용공여기간_카드론                   			AS 청구비용_정상론
            , (T1.이용회원수_신판 - T1.이용회원수_할부 + T1.이용회원수_할부 * T4.신용공여기간_할부) * T4.청구비용       AS 청구비용_신판  
            , (T1.이용회원수_기본 - T1.이용회원수_할부 + T1.이용회원수_할부 * T4.신용공여기간_할부) * T4.청구비용       AS 청구비용_기본
            , 0														AS 입금비용_일시불                 
            , 0														AS 입금비용_할부                   
            , 0														AS 입금비용_CA                     
            , 0														AS 입금비용_정상론                 
            , 0														AS 입금비용_신판                   
            , 0														AS 입금비용_기본                   
            , 0														AS PROCESS비용_일시불              
            , 0														AS PROCESS비용_할부                
            , 0														AS PROCESS비용_CA                  
            , 0														AS PROCESS비용_정상론              
            , 0														AS PROCESS비용_신판                
            , 0														AS PROCESS비용_기본                
            , T1.이용금액_개인_일시불 * T4.M적립취급비중_일시불    * T4.M포인트비율_일시불				AS M포인트비용_일시불              
            , T1.이용금액_개인_할부   * T4.M적립취급비중_할부      * T4.M포인트비율_할부 				AS M포인트비용_할부
            , T1.이용금액_개인_일시불 * T4.마일리지취급비중_일시불 * T4.마일리지비율_일시불				AS 마일리지비용_일시불             
            , T1.이용금액_개인_할부   * T4.마일리지취급비중_할부   * T4.마일리지비율_할부				AS 마일리지비용_할부               
            , T1.이용금액_개인_일시불 * T4.할인서비스비율    								AS 할인서비스_일시불               
            , T1.이용금액_개인_할부   * T4.할인서비스비율      								AS 할인서비스_할부                 
            , T1.이용금액_개인_일시불 * T4.판촉서비스비율 								AS 판촉서비스수수료_일시불         
            , T1.이용금액_개인_할부   * T4.판촉서비스비율   								AS 판촉서비스수수료_할부           
            , T1.이용금액_개인_일시불 * T4.브랜드로얄티비율_일시불							AS 브랜드로열티_일시불             
            , T1.이용금액_개인_할부   * T4.브랜드로얄티비율_할부   							AS 브랜드로열티_할부               
            , T1.이용금액_개인_CA     * T4.브랜드로얄티비율_CA   							AS 브랜드로열티_CA                 
            , T1.이용건수_개인_정상론 * T4.자금이체수수료   								AS 자금이체수수료_정상론           
            , 0														AS 상품서비스비용_일시불           
            , 0														AS 상품서비스비용_할부             
            , T3.대손충당비용_일시불											AS 대손충당비용_일시불                 
            , T3.대손충당비용_할부  											AS 대손충당비용_할부                   
            , T3.대손충당비용_CA    											AS 대손충당비용_CA                     
            , T3.대손충당비용_정상론											AS 대손충당비용_정상론                 
            , 0														AS 공헌이익_일시불                 
            , 0														AS 공헌이익_할부                   
            , 0														AS 공헌이익_CA                     
            , 0														AS 공헌이익_정상론                 
            , 0														AS 공헌이익_신판                   
            , 0														AS 공헌이익_기본                   
            , 0														AS 공헌이익율_일시불               
            , 0														AS 공헌이익율_할부                 
            , 0														AS 공헌이익율_CA                   
            , 0														AS 공헌이익율_정상론               
            , 0														AS 공헌이익율_신판                 
            , 0														AS 공헌이익율_기본			
            , SYSDATE													AS DM적재일시                      
            
 
        FROM  TEMPACRM_월개인본인회원실적합계       T1,
              TEMPACRM_대손비용                 T3,
              ACRM.CRM단가                      T4

       WHERE  T4.기준년월  = '$1'  ;

      COMMIT;

